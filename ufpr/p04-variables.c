#include <stdio.h>

void main() {

  int nValorInt;

  nValorInt = 1;
  printf("Valor = %d\n", nValorInt);
  nValorInt = nValorInt - 1;
  printf("Valor = %d\n", nValorInt);
  nValorInt -= 1;
  printf("Valor = %d\n", nValorInt);
  nValorInt--;
  printf("Valor = %d\n", nValorInt);
  nValorInt = nValorInt + 1;
  printf("Valor = %d\n", nValorInt);
  nValorInt += 1;
  printf("Valor = %d\n", nValorInt);
  nValorInt++;
  printf("Valor = %d\n", nValorInt);

}
